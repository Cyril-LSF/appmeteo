<?php

namespace Controllers;

class Weather {

    public function index(){

        $pageTitle = "Accueil";

        \Renderer::render('weather/index', compact('pageTitle'));

    }

    public function show(){

        $location = null;
        if(!empty($_POST['location'])){
            $search  = array('À', 'Á', 'Â', 'Ã', 'Ä', 'Å', 'Ç', 'È', 'É', 'Ê', 'Ë', 'Ì', 'Í', 'Î', 'Ï', 'Ò', 'Ó', 'Ô', 'Õ', 'Ö', 'Ù', 'Ú', 'Û', 'Ü', 'Ý', 'à', 'á', 'â', 'ã', 'ä', 'å', 'ç', 'è', 'é', 'ê', 'ë', 'ì', 'í', 'î', 'ï', 'ð', 'ò', 'ó', 'ô', 'õ', 'ö', 'ù', 'ú', 'û', 'ü', 'ý', 'ÿ');
	        $replace = array('A', 'A', 'A', 'A', 'A', 'A', 'C', 'E', 'E', 'E', 'E', 'I', 'I', 'I', 'I', 'O', 'O', 'O', 'O', 'O', 'U', 'U', 'U', 'U', 'Y', 'a', 'a', 'a', 'a', 'a', 'a', 'c', 'e', 'e', 'e', 'e', 'i', 'i', 'i', 'i', 'o', 'o', 'o', 'o', 'o', 'o', 'u', 'u', 'u', 'u', 'y', 'y');
	        $location = str_replace($search, $replace, $_POST['location']);
        } else {
            die("Veuillez saisir une ville");
        }

        $weather = \Services\Weather::getWeather($location);
        if(!$weather){
            die("La ville n'existe pas");
        }

        $pageTitle = $weather['request']['query'];

        \Renderer::render('weather/show', compact('pageTitle', 'weather'));

    }

}